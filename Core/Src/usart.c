/**
  * 函数名称：串口中断回调函数
  * 函数功能：调用回调函数的串口
  * 形   参：串口名
  * 返回值 ：无
  */

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
		static uint8_t pixel_data = 0;
    if(huart->Instance==USART3)
    {
        if (UART3_temp[0] == '(')
				{ 
					uar_printf_IT("START \n\r");
					state = 1;
				}
				else if (state == 1 && UART3_temp[0] >= '0' && UART3_temp[0] <= '9'){
					pixel_data = pixel_data*10+UART3_temp[0]-48;
				}
				else if (state == 1 && UART3_temp[0] == ',')
				{
					UART3_Rx_Buf[UART3_Rx_cnt]=pixel_data;
					UART3_Rx_cnt++;
					pixel_data = 0;
				}
				else if (UART3_temp[0] == ')')
				{
					state = 3;
//					if(UART3_Rx_cnt==28*28){
//						uar_printf_IT("show!\n");
//						lcd_displayimage032_zoom(UART3_Rx_Buf, 28, 28, 40, 40);
//					}
					UART3_Rx_cnt = 0;
					//state = 0;
				}
        HAL_UART_Receive_IT(&huart3,(uint8_t *)UART3_temp,Rec_Long); // 重新开启串口接收中断
    }
}
